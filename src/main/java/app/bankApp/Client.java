package app.bankApp;

public class Client {
    private String name;
    private String lastName;
    private String age;
    private String id;
    private String password;

    public Client(String name, String lastName, String age, String id, String password) {
        this.name = name;
        this.lastName = lastName;
        this.age = age;
        this.id = id;
        this.password = password;
    }
}
